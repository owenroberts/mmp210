var heading9 = "Diving";
var diving = "Before a race starts, the swimmers stand on platforms and dive when the race starts. To be able to dive effectively you have to jump into the water head first with your arms straight and your hands together, your head has to be positioned under your arms so your face doesn't hit the water which can slow you down, and keep your body straight. The angle at which you dive is also important because you don't want to dive too deep because then it will take longer for you to reach the surface of the water and if you dive too high you will belly flop or something. After you dive keep the same stance as when you dove but start performing dolphin kicks to move forward as well as to reach the surface of the water to begin swimming. Too many dolphin kicks can lead to a disqualification. Personally I dolphin kick twice and try to reach the surface as soon as possible."
        
